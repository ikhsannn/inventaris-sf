    public void loadNumber(){
        try{
            String bulan = jTextField2.getText();
            String nobm = "BM" + bulan.substring(6, 10) + bulan.substring(3, 5);
            connect.config();
            String sqlLNBM = "select * from BMHeader where nobm like '" + nobm + "%'";
            Statement stmt = connect.konek.createStatement();
            ResultSet rs = stmt.executeQuery(sqlLNBM);
            if(rs.last()){
                String no = rs.getString("nobm").substring(8, 12);
                String an = "" + (Integer.parseInt(no)+1);
                String nol = "";
                switch(an.length()){
                    case 1:
                        nol = "000";
                        break;
                    case 2:
                        nol = "00";
                        break;
                    case 3:
                        nol = "0";
                        break;
                    case 4:
                        nol = "";
                        break;
                }
                nobm = nobm +nol + an;
            }else{
                nobm = nobm + "0001";
            }
            jTextField1.setText(nobm);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Data  tidak terhubung... " + e.getMessage());
        }
    }
    
