    public void searchData(){
        try{
            int totqty = 0;
            double totsel = 0;
            connect.config();
            String sqlSBMH ="select * from bmheader where nobm = '" +jTextField1.getText() + "'";
            
            Statement stSBMH = connect.konek.createStatement();
            ResultSet rsSBMH = stSBMH.executeQuery(sqlSBMH);
            while(rsSBMH.next()){
                jTextField2.setText(rsSBMH.getString("tgl"));
                jTextField3.setText(rsSBMH.getString("supid"));
                jTextArea1.setText(rsSBMH.getString("keterangan"));
            }
            findSupplier();
            

            String sqlSBMD ="select * from bmdetail where nobm = '" +jTextField1.getText() + "'";
            String header[] = {"Kode","Nama","Satuan","Harga","Jumlah","Total"};

            Statement stSBMD = connect.konek.createStatement();
            ResultSet rsSBMD = stSBMH.executeQuery(sqlSBMD);
            int col = 6;
            int brs=0;
            while(rsSBMD.next()){
                brs = rsSBMD.getRow();
            }
            
            Object dataTable[][]= new Object[brs][col];
            rsSBMD.beforeFirst();
            int x =0;
            while(rsSBMD.next()){
                double total = 0;
                String kode =  rsSBMD.getString("kodebarang");
                dataTable[x][0] = rsSBMD.getString("kodebarang");
                dataTable[x][1] = connect.isiNMBarang(kode);
//                dataTable[x][1] = namabrg(rsSBMD.getString("kodebarang"));
                dataTable[x][2] = connect.isiSatuan(kode);
                dataTable[x][3] = rsSBMD.getString("harga");
                dataTable[x][4] = rsSBMD.getString("jumlah");
                total = Double.valueOf(rsSBMD.getString("harga")) * Double.valueOf(rsSBMD.getString("jumlah"));
                dataTable[x][5] = String.valueOf(total);
                totqty = totqty + Integer.parseInt(rsSBMD.getString("jumlah"));
                totsel = totsel + total;
                x++;
            }
            jTextField11.setText(String.valueOf(totqty));
            jTextField12.setText(String.valueOf(totsel));
            jTable1.setModel(new DefaultTableModel(dataTable, header));
            stSBMD.close();

        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Data tidak terhubung..." + e.getMessage());
        }
    }

